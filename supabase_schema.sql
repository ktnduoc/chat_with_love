-- 1. Create Profiles Table (Lưu trữ danh tính hai bạn)
-- Chỗ này lưu tên biệt danh, ảnh đại diện và thời gian cập nhật.
CREATE TABLE IF NOT EXISTS public.profiles (
  id UUID REFERENCES auth.users NOT NULL PRIMARY KEY,
  username TEXT UNIQUE,
  avatar_url TEXT,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- 2. Create Messages Table (Lưu trữ kỉ niệm vĩnh viễn)
-- Chỗ này lưu nội dung chat, link ảnh/sticker và thời gian gởi.
CREATE TABLE IF NOT EXISTS public.messages (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  sender_id UUID REFERENCES auth.users NOT NULL,
  content TEXT,
  image_url TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  is_read BOOLEAN DEFAULT FALSE
);

-- 3. Check & Enable Realtime (Dòng này có thể bỏ qua nếu đã chạy rồi)
-- Lệnh này cực kỳ quan trọng để tin nhắn nhảy lên ngay lập tức.
