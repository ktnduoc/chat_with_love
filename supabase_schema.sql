-- 4. Set up Row Level Security (RLS - Bảo mật không gian riêng tư)
-- Đảm bảo không ai có thể xâm nhập vào hốc nhỏ bí mật của hai bạn.
ALTER TABLE public.profiles ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.messages ENABLE ROW LEVEL SECURITY;

-- Quyền đọc Profile: Cho phép hai bạn thấy tên/ảnh của nhau.
CREATE POLICY "Public profiles are viewable by everyone." ON public.profiles
  FOR SELECT USING (true);

-- Quyền cập nhật Profile: Ai chỉ được sửa ảnh/tên của người đó.
CREATE POLICY "Users can update own profile." ON public.profiles
  FOR UPDATE USING (auth.uid() = id);

-- Quyền đọc tin nhắn: Chỉ những người đã đăng nhập mới được đọc.
CREATE POLICY "Authenticated users can read messages." ON public.messages
  FOR SELECT TO authenticated USING (true);

-- Quyền gởi tin nhắn: Chỉ người đã đăng nhập mới được gởi dưới tên mình.
CREATE POLICY "Authenticated users can insert messages." ON public.messages
  FOR INSERT TO authenticated WITH CHECK (auth.uid() = sender_id);
