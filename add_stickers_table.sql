CREATE TABLE IF NOT EXISTS public.love_streaks (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_low UUID REFERENCES auth.users NOT NULL,
  user_high UUID REFERENCES auth.users NOT NULL,
  current_streak INTEGER NOT NULL DEFAULT 0,
  longest_streak INTEGER NOT NULL DEFAULT 0,
  last_mutual_day DATE,
  today_day DATE,
  today_has_low BOOLEAN NOT NULL DEFAULT FALSE,
  today_has_high BOOLEAN NOT NULL DEFAULT FALSE,
  updated_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  CONSTRAINT love_streaks_pair_unique UNIQUE (user_low, user_high),
  CONSTRAINT love_streaks_pair_check CHECK (user_low <> user_high)
);

DO $$
BEGIN
  ALTER PUBLICATION supabase_realtime ADD TABLE public.love_streaks;
EXCEPTION
  WHEN duplicate_object THEN NULL;
  WHEN undefined_object THEN NULL;
END $$;

ALTER TABLE public.love_streaks ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Users can view own love streak rows." ON public.love_streaks;
DROP POLICY IF EXISTS "Users can insert own love streak rows." ON public.love_streaks;
DROP POLICY IF EXISTS "Users can update own love streak rows." ON public.love_streaks;

CREATE POLICY "Users can view own love streak rows." ON public.love_streaks
  FOR SELECT TO authenticated
  USING (auth.uid() = user_low OR auth.uid() = user_high);

CREATE POLICY "Users can insert own love streak rows." ON public.love_streaks
  FOR INSERT TO authenticated
  WITH CHECK (auth.uid() = user_low OR auth.uid() = user_high);

CREATE POLICY "Users can update own love streak rows." ON public.love_streaks
  FOR UPDATE TO authenticated
  USING (auth.uid() = user_low OR auth.uid() = user_high)
  WITH CHECK (auth.uid() = user_low OR auth.uid() = user_high);

CREATE OR REPLACE FUNCTION public.bump_love_streak_from_message()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
  v_low UUID;
  v_high UUID;
  v_msg_day DATE;
  v_row public.love_streaks%ROWTYPE;
  v_today_low BOOLEAN;
  v_today_high BOOLEAN;
  v_new_streak INTEGER;
BEGIN
  IF NEW.sender_id IS NULL OR NEW.receiver_id IS NULL OR NEW.sender_id = NEW.receiver_id THEN
    RETURN NEW;
  END IF;

  IF NEW.sender_id::text < NEW.receiver_id::text THEN
    v_low := NEW.sender_id;
    v_high := NEW.receiver_id;
  ELSE
    v_low := NEW.receiver_id;
    v_high := NEW.sender_id;
  END IF;

  v_msg_day := (NEW.created_at AT TIME ZONE 'utc')::date;

  INSERT INTO public.love_streaks (user_low, user_high, today_day, updated_at)
  VALUES (v_low, v_high, v_msg_day, timezone('utc'::text, now()))
  ON CONFLICT (user_low, user_high) DO NOTHING;

  SELECT *
  INTO v_row
  FROM public.love_streaks
  WHERE user_low = v_low AND user_high = v_high
  FOR UPDATE;

  IF v_row.today_day IS NULL OR v_row.today_day < v_msg_day THEN
    v_row.today_day := v_msg_day;
    v_row.today_has_low := FALSE;
    v_row.today_has_high := FALSE;
  END IF;

  IF NEW.sender_id = v_low THEN
    v_today_low := TRUE;
    v_today_high := v_row.today_has_high;
  ELSE
    v_today_low := v_row.today_has_low;
    v_today_high := TRUE;
  END IF;

  v_new_streak := v_row.current_streak;

  IF v_today_low AND v_today_high AND v_row.last_mutual_day IS DISTINCT FROM v_row.today_day THEN
    IF v_row.last_mutual_day = (v_row.today_day - INTERVAL '1 day')::date THEN
      v_new_streak := GREATEST(1, v_row.current_streak + 1);
    ELSE
      v_new_streak := 1;
    END IF;
  END IF;

  UPDATE public.love_streaks
  SET
    today_day = v_row.today_day,
    today_has_low = v_today_low,
    today_has_high = v_today_high,
    current_streak = CASE
      WHEN v_today_low AND v_today_high AND v_row.last_mutual_day IS DISTINCT FROM v_row.today_day
        THEN v_new_streak
      ELSE v_row.current_streak
    END,
    longest_streak = GREATEST(v_row.longest_streak, CASE
      WHEN v_today_low AND v_today_high AND v_row.last_mutual_day IS DISTINCT FROM v_row.today_day
        THEN v_new_streak
      ELSE v_row.current_streak
    END),
    last_mutual_day = CASE
      WHEN v_today_low AND v_today_high AND v_row.last_mutual_day IS DISTINCT FROM v_row.today_day
        THEN v_row.today_day
      ELSE v_row.last_mutual_day
    END,
    updated_at = timezone('utc'::text, now())
  WHERE user_low = v_low AND user_high = v_high;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_bump_love_streak_from_message ON public.messages;

CREATE TRIGGER trg_bump_love_streak_from_message
AFTER INSERT ON public.messages
FOR EACH ROW
EXECUTE FUNCTION public.bump_love_streak_from_message();
