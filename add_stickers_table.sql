-- -------------------------------------------------------------
-- 🗑️ MESSAGE DELETE REQUESTS: XIN CHẤP NHẬN TRƯỚC KHI XÓA
-- -------------------------------------------------------------
CREATE TABLE IF NOT EXISTS public.message_delete_requests (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  message_id UUID REFERENCES public.messages(id) ON DELETE CASCADE NOT NULL,
  requester_id UUID REFERENCES auth.users NOT NULL,
  approver_id UUID REFERENCES auth.users NOT NULL,
  status TEXT NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'accepted', 'rejected')),
  created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
  responded_at TIMESTAMP WITH TIME ZONE
);

DO $$
BEGIN
  ALTER PUBLICATION supabase_realtime ADD TABLE public.message_delete_requests;
EXCEPTION
  WHEN duplicate_object THEN NULL;
  WHEN undefined_object THEN NULL;
END $$;

CREATE UNIQUE INDEX IF NOT EXISTS message_delete_requests_pending_unique
  ON public.message_delete_requests(message_id)
  WHERE status = 'pending';

ALTER TABLE public.message_delete_requests ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Users can view own delete requests." ON public.message_delete_requests;
DROP POLICY IF EXISTS "Requester can create delete request." ON public.message_delete_requests;
DROP POLICY IF EXISTS "Approver can respond delete request." ON public.message_delete_requests;
DROP POLICY IF EXISTS "Requester can cancel own pending request." ON public.message_delete_requests;

CREATE POLICY "Users can view own delete requests." ON public.message_delete_requests
  FOR SELECT TO authenticated
  USING (auth.uid() = requester_id OR auth.uid() = approver_id);

CREATE POLICY "Requester can create delete request." ON public.message_delete_requests
  FOR INSERT TO authenticated
  WITH CHECK (auth.uid() = requester_id);

CREATE POLICY "Approver can respond delete request." ON public.message_delete_requests
  FOR UPDATE TO authenticated
  USING (auth.uid() = approver_id)
  WITH CHECK (auth.uid() = approver_id);

CREATE POLICY "Requester can cancel own pending request." ON public.message_delete_requests
  FOR DELETE TO authenticated
  USING (auth.uid() = requester_id AND status = 'pending');

-- -------------------------------------------------------------
-- ✅ MESSAGE DELETE EXECUTION: CHO PHÉP XÓA SAU KHI ĐƯỢC DUYỆT
-- -------------------------------------------------------------
DROP POLICY IF EXISTS "Users can delete approved messages." ON public.messages;

CREATE POLICY "Users can delete approved messages." ON public.messages
  FOR DELETE TO authenticated
  USING (
    auth.uid() = sender_id
    OR EXISTS (
      SELECT 1
      FROM public.message_delete_requests mdr
      WHERE mdr.message_id = messages.id
        AND mdr.approver_id = auth.uid()
        AND mdr.status = 'accepted'
    )
  );

